#!/usr/bin/env node
import http from 'http';
import { execSync } from 'child_process';
import { fileURLToPath } from 'url';
import { dirname, resolve } from 'path';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_PATH = resolve(__dirname, '../..');

const PORT = 8082;
const WORKER_POOL_URL = 'http://localhost:8081';

const server = http.createServer(async (req, res) => {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET, POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') {
    res.writeHead(200);
    res.end();
    return;
  }

  if (req.url === '/health' && req.method === 'GET') {
    res.writeHead(200, { 'Content-Type': 'application/json' });
    res.end(JSON.stringify({ status: 'ok', uptime: process.uptime() }));
    return;
  }

  if (req.url === '/api/process-pr' && req.method === 'POST') {
    let body = '';
    req.on('data', chunk => body += chunk.toString());
    req.on('end', () => {
      try {
        const { prNumber, branch, repo, owner, taskDetails } = JSON.parse(body);
        
        console.log(`üìã Processing PR #${prNumber} on branch ${branch}`);
        
        res.writeHead(202, { 'Content-Type': 'application/json' });
        res.end(JSON.stringify({ status: 'accepted', prNumber, branch }));
        
        setImmediate(() => processInBackground(prNumber, branch, repo, owner, taskDetails));
      } catch (error) {
        console.error(`‚ùå Parse error:`, error.message);
        res.writeHead(400, { 'Content-Type': 'application/json' });
        res.end(JSON.stringify({ error: error.message }));
      }
    });
    return;
  }

  res.writeHead(404);
  res.end('Not Found');
});

async function processInBackground(prNumber, branch, repo, owner, taskDetails) {
  const startTime = Date.now();
  console.log(`\n${'='.repeat(80)}`);
  console.log(`üöÄ Starting PR processing: #${prNumber}`);
  console.log(`   Branch: ${branch}`);
  console.log(`   Time: ${new Date().toISOString()}`);
  console.log(`${'='.repeat(80)}\n`);
  
  try {
    const repoPath = REPO_PATH;
    
    // Step 1: Clean up stuck operations
    console.log('üìã Step 1: Checking for stuck git operations...');
    try {
      execSync('git rebase --abort', { cwd: repoPath, stdio: 'ignore' });
      console.log('   ‚ö†Ô∏è  Aborted stuck rebase');
    } catch (e) {
      console.log('   ‚úÖ No stuck rebase');
    }
    try {
      execSync('git merge --abort', { cwd: repoPath, stdio: 'ignore' });
      console.log('   ‚ö†Ô∏è  Aborted stuck merge');
    } catch (e) {
      console.log('   ‚úÖ No stuck merge');
    }
    
    // Step 2: Stash any local changes
    console.log('\nüìã Step 2: Stashing local changes...');
    try {
      const stashResult = execSync('git stash', { cwd: repoPath, encoding: 'utf8' });
      console.log(`   ${stashResult.includes('No local changes') ? '‚úÖ' : '‚ö†Ô∏è'} ${stashResult.trim()}`);
    } catch (e) {
      console.log('   ‚úÖ Nothing to stash');
    }
    
    // Step 3: Fetch branch
    console.log(`\nüìã Step 3: Fetching branch ${branch}...`);
    execSync(`git fetch origin ${branch}`, { cwd: repoPath, stdio: 'inherit' });
    console.log('   ‚úÖ Fetch complete');
    
    // Step 4: Checkout branch
    console.log(`\nüìã Step 4: Checking out branch ${branch}...`);
    execSync(`git checkout ${branch}`, { cwd: repoPath, stdio: 'inherit' });
    console.log('   ‚úÖ Checkout complete');
    
    console.log(`‚úÖ Checked out branch ${branch}`);
    
    // Step 5: Call Kiro to generate code
    console.log(`\nüìã Step 5: Calling Kiro API to generate code...`);
    console.log(`   Worker Pool URL: ${WORKER_POOL_URL}`);
    console.log(`   Prompt: ${taskDetails.substring(0, 100)}...`);
    console.log(`   Timeout: 300s`);
    
    const kiroStartTime = Date.now();
    const response = await fetch(`${WORKER_POOL_URL}/execute`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        prompt: `Read TASK.md and implement the feature. ${taskDetails}`,
        timeoutMs: 300000
      })
    });
    
    const result = await response.json();
    const kiroElapsed = ((Date.now() - kiroStartTime) / 1000).toFixed(1);
    console.log(`   ‚è±Ô∏è  Kiro execution time: ${kiroElapsed}s`);
    console.log(`   Result: ${result.success ? '‚úÖ Success' : '‚ùå Failed'}`);
    if (result.error) {
      console.log(`   Error: ${result.error}`);
    }
    
    if (result.success) {
      console.log(`‚úÖ Kiro completed for PR #${prNumber}`);
      
      // Step 6: Check for changes
      console.log(`\nüìã Step 6: Checking for generated changes...`);
      try {
        const statusOutput = execSync('git status --porcelain', { cwd: repoPath, encoding: 'utf8' });
        if (!statusOutput.trim()) {
          console.log(`   ‚ö†Ô∏è  No changes generated by Kiro`);
          console.log(`   This could mean:`);
          console.log(`   - Kiro didn't understand the task`);
          console.log(`   - The feature already exists`);
          console.log(`   - Kiro encountered an error but returned success`);
          console.log(`\n${'='.repeat(80)}`);
          console.log(`‚ùå PR #${prNumber} processing failed: No changes generated`);
          console.log(`   Total time: ${((Date.now() - startTime) / 1000).toFixed(1)}s`);
          console.log(`${'='.repeat(80)}\n`);
          return;
        }
        
        console.log(`   ‚úÖ Changes detected:`);
        statusOutput.split('\n').forEach(line => {
          if (line.trim()) console.log(`      ${line}`);
        });
        
        // Step 7: Stage changes
        console.log(`\nüìã Step 7: Staging changes...`);
        execSync('git add -A', { cwd: repoPath });
        const diffStat = execSync('git diff --cached --stat', { cwd: repoPath, encoding: 'utf8' });
        console.log(`   ${diffStat}`);
        console.log('   ‚úÖ Changes staged');
        
        // Step 8: Commit changes
        console.log(`\nüìã Step 8: Committing changes...`);
        execSync('git commit -m "feat: implement feature via Kiro CLI"', { cwd: repoPath });
        const commitHash = execSync('git rev-parse HEAD', { cwd: repoPath, encoding: 'utf8' }).trim();
        console.log(`   ‚úÖ Committed: ${commitHash.substring(0, 7)}`);
        
        // Step 9: Push to GitHub
        console.log(`\nüìã Step 9: Pushing to GitHub...`);
        execSync(`git push origin ${branch}`, { cwd: repoPath });
        console.log(`   ‚úÖ Pushed to origin/${branch}`);
        
        console.log(`\n${'='.repeat(80)}`);
        console.log(`‚úÖ PR #${prNumber} processing completed successfully`);
        console.log(`   Total time: ${((Date.now() - startTime) / 1000).toFixed(1)}s`);
        console.log(`   Kiro time: ${kiroElapsed}s`);
        console.log(`   Commit: ${commitHash.substring(0, 7)}`);
        console.log(`${'='.repeat(80)}\n`);
      } catch (e) {
        console.log(`\n${'='.repeat(80)}`);
        console.error(`‚ùå Git operations failed for PR #${prNumber}`);
        console.error(`   Error: ${e.message}`);
        console.error(`   Command: ${e.cmd || 'unknown'}`);
        if (e.stderr) {
          console.error(`   Stderr: ${e.stderr.toString()}`);
        }
        console.log(`   Total time: ${((Date.now() - startTime) / 1000).toFixed(1)}s`);
        console.log(`${'='.repeat(80)}\n`);
      }
    } else {
      console.log(`\n${'='.repeat(80)}`);
      console.error(`‚ùå Kiro failed for PR #${prNumber}`);
      console.error(`   Error: ${result.error || 'Unknown error'}`);
      console.log(`   Total time: ${((Date.now() - startTime) / 1000).toFixed(1)}s`);
      console.log(`${'='.repeat(80)}\n`);
    }
  } catch (error) {
    console.log(`\n${'='.repeat(80)}`);
    console.error(`‚ùå PR processing failed for #${prNumber}`);
    console.error(`   Error: ${error.message}`);
    console.error(`   Stack: ${error.stack}`);
    console.log(`   Total time: ${((Date.now() - startTime) / 1000).toFixed(1)}s`);
    console.log(`${'='.repeat(80)}\n`);
  }
}

function tryListen(port) {
  server.listen(port, () => {
    console.log(`üöÄ PR Processor API on port ${port}`);
  }).on('error', (err) => {
    if (err.code === 'EADDRINUSE') {
      console.log(`‚ö†Ô∏è  Port ${port} in use, trying ${port + 1}`);
      tryListen(port + 1);
    } else {
      throw err;
    }
  });
}

tryListen(PORT);
